def square(n):
    """
    should return numbers that can be arranged into square geometric shapes
    """
    return n ** 2


def triangle(n):
    """
    should return numbers that can be arranged in triangular geometic shapes
    """
    return n * (n + 1) / 2
    


def cube(n):
    """
    should return numbers that can be arranged as symmetric cube shapes
    """
    return n ** 3
